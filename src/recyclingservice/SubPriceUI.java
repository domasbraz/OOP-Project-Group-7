    private void purchaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseBtnActionPerformed
        if(subTextField.getText().equalsIgnoreCase("Bronze")){
           if(S.ShowBalance() <= 29){
               JOptionPane.showMessageDialog(null, "You do not have enough funds to purchase the Bronze subscription. Please top up your balance to proceed with the purchase.");
           }
           else{
                S.BronzePurchase();
                JOptionPane.showMessageDialog(null, "You have just purchased the Bronze Subscription");
           }
        }else if(subTextField.getText().equalsIgnoreCase("Silver")){
           if(S.ShowBalance() <= 49){
               JOptionPane.showMessageDialog(null, "You do not have enough funds to purchase the Silver subscription. Please top up your balance to proceed with the purchase.");
           }else{
                S.SilverPurchase();
                JOptionPane.showMessageDialog(null, "You have just purchased the Silver Subscription");
           }
        }
        else if(subTextField.getText().equalsIgnoreCase("Gold")){
           if(S.ShowBalance() <= 79){
            JOptionPane.showMessageDialog(null, "You do not have enough funds to purchase the Gold subscription. Please top up your balance to proceed with the purchase.");
           }
           else{
               S.GoldPurchase();
               JOptionPane.showMessageDialog(null, "You have just purchased the Gold Subscription");       
           }  
        }
    }//GEN-LAST:event_purchaseBtnActionPerformed

