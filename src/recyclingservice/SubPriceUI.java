    private void BalanceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BalanceBtnActionPerformed
        int ans = Integer.parseInt(JOptionPane.showInputDialog(null,"Press the number shown to:\n1) Add Balance\n2) Subtract Balance\n3) View Balance"));
        switch(ans){
            case 1 : input = Integer.parseInt(JOptionPane.showInputDialog(null,"Enter the amount you would like to add to your account."));
                     S.setBalance(input);
                     S.AddBalance();
                     JOptionPane.showMessageDialog(null,"You added €"+input+" to your account."+"\nYou have €"+S.ShowBalance()+" in your account currently.");
                break;
            case 2 : input = Integer.parseInt(JOptionPane.showInputDialog(null,"Enter the amount you would like to subtract from your account."));
                     S.setBalance(input);
                     S.SubBalance();
                     if(S.ShowBalance() >= 1){
                         JOptionPane.showMessageDialog(null,"You removed €"+input+" to your account."+"\nYou now have €"+S.ShowBalance()+" in your account currently.");
                     }else if(S.ShowBalance() == 0){
                        JOptionPane.showMessageDialog(null, "You do not have anything in your account to remove. Please top up.");
                     }
                break;
            case 3 : JOptionPane.showMessageDialog(null,"You have €"+S.ShowBalance()+" in your account currently.");
                break;
            default: if(ans != 1 || ans != 2 || ans != 3){JOptionPane.showMessageDialog(null, "Please only enter a number shown.");}
        }
    }//GEN-LAST:event_BalanceBtnActionPerformed

