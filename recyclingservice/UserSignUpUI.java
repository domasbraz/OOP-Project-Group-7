    private void displayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBtnActionPerformed
        if(Accounts.size() == 0){
            changingLabel.setText("There are no accounts created to view");
        }
        for(int i=0; i < Accounts.size(); i++){
            JOptionPane.showMessageDialog(null, "Here are your account(s) details."+"\n Name: "+Accounts.get(i).getName()+"\n Email: "+Accounts.get(i).getEmail()+"\n Address: "+Accounts.get(i).getAddress()+"\n Phone: "+Accounts.get(i).getPhone());
        }
    }//GEN-LAST:event_displayBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if(nameTextField.getText().equals("") || emailTextField.getText().equals("") || addressTextField.getText().equals("") || phoneTextField.getText().equals("") || passwordTextField.getText().equals("")){
            changingLabel.setText("You must first create an account");
        }
        else if(count == 0){
            changingLabel.setText("There are no accounts to delete");
        }
        else{
            name = nameTextField.getText();
            email = emailTextField.getText(); 
            address = addressTextField.getText(); 
            phone = phoneTextField.getText(); 
            password = passwordTextField.getText(); 
            
            for(int i=0; i < Accounts.size(); i++){
                if(Accounts.get(i).getName().equals(name)&& Accounts.get(i).getEmail().equals(email)&& Accounts.get(i).getAddress().equals(address)&& Accounts.get(i).getPhone().equals(phone)&&Accounts.get(i).getPassword().equals(password)){
                    Accounts.remove(i);
                    count = count - 1;
                    changingLabel.setText("Account User "+i+" Successfully Deleted.");
                    break;
                }
                if(count - 1 == i){
                    changingLabel.setText("That account could not be found.");
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

