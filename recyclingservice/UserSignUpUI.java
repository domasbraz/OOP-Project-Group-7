    private void signUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpBtnActionPerformed
        if(nameTextField.getText().equals("") || emailTextField.getText().equals("") || addressTextField.getText().equals("") || phoneTextField.getText().equals("") || passwordTextField.getText().equals("")){
            changingLabel.setText("You must enter your full details to make an account");   
        }
        else if(nameTextField.getText() != "" && emailTextField.getText()!= "" && addressTextField.getText()!= "" && phoneTextField.getText()!= "" && passwordTextField.getText()!= ""){
            name = nameTextField.getText();
            email = emailTextField.getText(); 
            address = addressTextField.getText(); 
            phone = phoneTextField.getText(); 
            password = passwordTextField.getText(); 

            UserDetails user = new UserDetails();
            user.setName(name);
            user.setEmail(email);
            user.setAddress(address);
            user.setPhone(phone);
            user.setPassword(password);

            Accounts.add(user);
            count++;
            changingLabel.setText("Your account has been created!");
        }
    }//GEN-LAST:event_signUpBtnActionPerformed

